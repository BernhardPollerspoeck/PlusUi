namespace PlusUi.core;

/// <summary>
/// Specifies the position of tabs in a TabControl.
/// </summary>
public enum TabPosition
{
    /// <summary>
    /// Tabs are positioned at the top of the TabControl.
    /// </summary>
    Top,

    /// <summary>
    /// Tabs are positioned at the bottom of the TabControl.
    /// </summary>
    Bottom,

    /// <summary>
    /// Tabs are positioned on the left side of the TabControl.
    /// </summary>
    Left,

    /// <summary>
    /// Tabs are positioned on the right side of the TabControl.
    /// </summary>
    Right
}
