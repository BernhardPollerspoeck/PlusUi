namespace PlusUi.core;

/// <summary>
/// Specifies the horizontal alignment of a title in a toolbar.
/// </summary>
public enum TitleAlignment
{
    /// <summary>
    /// Title is aligned to the left (Material Design / Android style).
    /// </summary>
    Left = 0,

    /// <summary>
    /// Title is centered (iOS style).
    /// </summary>
    Center = 1
}
