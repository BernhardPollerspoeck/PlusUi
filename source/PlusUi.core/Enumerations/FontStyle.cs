namespace PlusUi.core;

/// <summary>
/// Specifies the style of a font.
/// </summary>
public enum FontStyle
{
    /// <summary>
    /// Normal upright font style.
    /// </summary>
    Normal,

    /// <summary>
    /// Italic font style. Uses the font's designed italic variant.
    /// </summary>
    Italic,

    /// <summary>
    /// Oblique font style. Slanted version of the normal font, used when true italic is not available.
    /// </summary>
    Oblique
}
