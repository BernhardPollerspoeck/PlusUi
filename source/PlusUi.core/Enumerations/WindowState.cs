namespace PlusUi.core;

/// <summary>
/// Specifies the state of a window.
/// </summary>
public enum WindowState
{
    /// <summary>
    /// The window is in its normal state.
    /// </summary>
    Normal = 0,

    /// <summary>
    /// The window is minimized.
    /// </summary>
    Minimized = 1,

    /// <summary>
    /// The window is maximized.
    /// </summary>
    Maximized = 2,

    /// <summary>
    /// The window is in fullscreen mode.
    /// </summary>
    Fullscreen = 3
}
