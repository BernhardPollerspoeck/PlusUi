namespace PlusUi.core;

/// <summary>
/// Specifies the orientation (direction) of a layout or control.
/// </summary>
public enum Orientation
{
    /// <summary>
    /// Vertical orientation. Elements are arranged from top to bottom.
    /// </summary>
    Vertical,

    /// <summary>
    /// Horizontal orientation. Elements are arranged from left to right.
    /// </summary>
    Horizontal
}
