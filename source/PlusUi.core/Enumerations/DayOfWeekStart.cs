namespace PlusUi.core;

/// <summary>
/// Specifies which day of the week is considered the first day.
/// </summary>
public enum DayOfWeekStart
{
    /// <summary>
    /// Sunday is the first day of the week (US convention).
    /// </summary>
    Sunday = 0,

    /// <summary>
    /// Monday is the first day of the week (European/ISO convention).
    /// </summary>
    Monday = 1
}
