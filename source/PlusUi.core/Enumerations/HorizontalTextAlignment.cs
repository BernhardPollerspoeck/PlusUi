namespace PlusUi.core;

/// <summary>
/// Specifies the horizontal alignment of text within a text element.
/// </summary>
public enum HorizontalTextAlignment
{
    /// <summary>
    /// Aligns text to the left.
    /// </summary>
    Left,

    /// <summary>
    /// Centers text horizontally.
    /// </summary>
    Center,

    /// <summary>
    /// Aligns text to the right.
    /// </summary>
    Right
}